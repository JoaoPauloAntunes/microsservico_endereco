    private void jFormattedTextFieldCepKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextFieldCepKeyReleased
        // String cep ="13419-200";
        // String cep ="30110-012";
        String cep = jFormattedTextFieldCep.getText().replaceAll("\\D", "");
        if (cep.length() == 8) {
            MicrosservicoEndereco me = new MicrosservicoEndereco();
            try {
                me.buscar(cep);
                jLabelEstado.setText(me.getEstado());
                jLabelCidade.setText(me.getCidade());
                jLabelBairro.setText(me.getBairro());
                jLabelComplemento.setText(me.getComplemento());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "CEP inv√°lido!");
            }
        }
    }//GEN-LAST:event_jFormattedTextFieldCepKeyReleased

